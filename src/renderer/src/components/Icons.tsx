import type { CSSProperties, ReactNode } from 'react'

interface IconProps {
  size?: number
  className?: string
  style?: CSSProperties
  strokeWidth?: number
}

function make(children: ReactNode, filled = false) {
  return function Icon({ size = 20, className, style, strokeWidth = 2 }: IconProps) {
    return (
      <svg
        width={size}
        height={size}
        viewBox="0 0 24 24"
        fill={filled ? 'currentColor' : 'none'}
        stroke="currentColor"
        strokeWidth={strokeWidth}
        strokeLinecap="round"
        strokeLinejoin="round"
        className={className}
        style={style}
        aria-hidden="true"
      >
        {children}
      </svg>
    )
  }
}

